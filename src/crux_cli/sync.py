def _build_http_bridge_entry(
    mcp_name: str,
    mcp_data: dict[str, Any],
    *,
    launcher_base: Path | None = None,
) -> dict[str, Any]:
    """Build a .mcp.json entry for an HTTP-transport MCP using the stdio-to-HTTP bridge."""
    launcher_base = launcher_base or launchers_dir()
    launcher_base.mkdir(parents=True, exist_ok=True)

    url = mcp_data.get("url", "")
    auth = mcp_data.get("auth", {})
    auth_type = auth.get("type", "")

    # Build env var export lines for auth
    export_lines = []
    export_lines.append(f'export CRUX_BRIDGE_URL="{url}"')

    if auth_type == "bearer":
        keychain_key = auth.get("keychain_key", "API_TOKEN")
        if not _SAFE_NAME_RE.match(mcp_name):
            msg = f"Unsafe MCP name for shell embedding: {mcp_name!r}"
            raise ValueError(msg)
        if not _SAFE_ENV_VAR_RE.match(keychain_key):
            msg = f"Unsafe keychain key for shell embedding: {keychain_key!r}"
            raise ValueError(msg)
        header_name = auth.get("header_name", "Authorization")
        header_prefix = auth.get("header_prefix", "Bearer")
        export_lines.append(f'export CRUX_BRIDGE_AUTH_HEADER="{header_name}"')
        export_lines.append(f'export CRUX_BRIDGE_AUTH_PREFIX="{header_prefix}"')
        export_lines.append('export CRUX_BRIDGE_AUTH_ENV="CRUX_AUTH_TOKEN"')
        export_lines.append(f"export CRUX_AUTH_TOKEN={_secret_lookup_command(mcp_name, keychain_key)}")
    elif auth_type in ("oauth", "oauth-client-credentials"):
        if not _SAFE_NAME_RE.match(mcp_name):
            msg = f"Unsafe MCP name for shell embedding: {mcp_name!r}"
            raise ValueError(msg)
        header_name = auth.get("header_name", "Authorization")
        header_prefix = auth.get("header_prefix", "Bearer")
        export_lines.append(f'export CRUX_BRIDGE_AUTH_HEADER="{header_name}"')
        export_lines.append(f'export CRUX_BRIDGE_AUTH_PREFIX="{header_prefix}"')
        export_lines.append('export CRUX_BRIDGE_AUTH_ENV="CRUX_AUTH_TOKEN"')
        export_lines.append(f"export CRUX_AUTH_TOKEN={_secret_lookup_command(mcp_name, 'access_token')}")

    exports = "\n".join(export_lines)

    script = f"""#!/bin/bash
# Auto-generated by crux sync — do not edit. Re-run 'crux project sync' to regenerate.
SCRIPT_DIR="$(cd "$(dirname "${{BASH_SOURCE[0]}}")" && pwd)"
{exports}
exec python3 -m crux_cli.bridge
"""
    safe_name = Path(mcp_name).name
    launcher_path = launcher_base / f"{safe_name}.sh"
    launcher_path.write_text(script)
    launcher_path.chmod(0o700)

    return {"command": str(launcher_path), "args": []}


def _build_server_entry(
    mcp_name: str,
    mcp_data: dict[str, Any],
    extra_args: list[str] | None = None,
    *,
    launcher_base: Path | None = None,
) -> dict[str, Any]:
    """Build a single mcpServers entry for .mcp.json."""
    # HTTP-transport MCPs use the bridge
    if mcp_data.get("type") == "streamable-http" or mcp_data.get("url"):
        return _build_http_bridge_entry(mcp_name, mcp_data, launcher_base=launcher_base)

    env_vars = mcp_data.get("auth", {}).get("env_vars", [])

    if env_vars:
        launcher = generate_launcher(mcp_name, mcp_data, launcher_base=launcher_base)
        return {"command": str(launcher), "args": extra_args or []}

    server: dict[str, Any] = {k: v for k, v in mcp_data.items() if k in ("command", "args", "env")}
    if extra_args:
        server["args"] = server.get("args", []) + extra_args

    if mcp_data.get("source_dir"):
        abs_source = mcp_data["source_dir"]
        if not Path(abs_source).is_absolute():
            abs_source = str(crux_home().parent / abs_source)
        server["args"] = [abs_source if a == "{source_dir}" else a for a in server.get("args", [])]

    return server


def sync_project(
    project_dir: Path,
    registry: dict[str, Any] | None = None,
    *,
    launcher_base: Path | None = None,
) -> tuple[bool, list[str]]:
    """Sync a single project: generate .mcp.json, copy skills."""
    crux_json_path = project_dir / "crux.json"
    if not crux_json_path.exists():
        return False, ["No crux.json found"]

    with open(crux_json_path) as f:
        crux_json = json.load(f)

    if registry is None:
        registry = _load_registry_for_sync()

    mcp_defs = registry.get("mcp_definitions", {})
    skill_defs = registry.get("skill_definitions", {})
    issues: list[str] = []

    # Build .mcp.json
    declared_mcps = crux_json.get("mcps", [])
    mcp_servers: dict[str, Any] = {}
    for mcp_entry in declared_mcps:
        if isinstance(mcp_entry, str):
            mcp_name, extra_args = mcp_entry, []
        else:
            mcp_name = mcp_entry.get("name", "")
            extra_args = mcp_entry.get("args", [])
        if mcp_name not in mcp_defs:
            issues.append(f"MCP '{mcp_name}' not found in registry")
            continue
        mcp_servers[mcp_name] = _build_server_entry(
            mcp_name, mcp_defs[mcp_name], extra_args or None, launcher_base=launcher_base
        )

    mcp_file = project_dir / ".mcp.json"
    new_config = {"mcpServers": mcp_servers}
    _atomic_json_write(mcp_file, new_config)

    # Copy skills
    declared_skills = crux_json.get("skills", [])
    for skill_name in declared_skills:
        if skill_name not in skill_defs:
            issues.append(f"Skill '{skill_name}' not found in registry")
            continue
        skill_data = skill_defs[skill_name]
        source_dir_str = skill_data.get("source_dir", "")
        source_path = Path(source_dir_str)
        if not source_path.is_absolute():
            candidate = skills_dir() / skill_name
            if candidate.exists():
                source_path = candidate
            elif source_dir_str:
                source_path = crux_home().parent / source_dir_str
            else:
                source_path = skills_dir() / skill_name

        safe_skill = Path(skill_name).name
        skills_parent = project_dir / ".claude" / "skills"
        dest_path = skills_parent / safe_skill
        if source_path.exists():
            import shutil

            if not dest_path.resolve().is_relative_to(skills_parent.resolve()):
                issues.append(f"Skill '{skill_name}' resolves outside skills directory")
                continue
            dest_path.parent.mkdir(parents=True, exist_ok=True)
            if dest_path.exists():
                shutil.rmtree(dest_path)
            shutil.copytree(source_path, dest_path)
        else:
            issues.append(f"Skill '{skill_name}' source missing at {source_path}")

    return True, issues


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _atomic_json_write(path: Path, data: dict[str, Any]) -> None:
    """Write JSON atomically."""
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(dir=path.parent, suffix=".tmp")
    try:
        with open(fd, "w") as f:
            json.dump(data, f, indent=2)
        Path(tmp).replace(path)
    except BaseException:
        Path(tmp).unlink(missing_ok=True)
        raise
