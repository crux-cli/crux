"""Sync engine — read crux.json + registry, generate .mcp.json and launcher scripts."""

from __future__ import annotations

import json
import platform
import re
import shlex
import tempfile
from pathlib import Path
from typing import Any

from crux_cli.paths import crux_home, launchers_dir, registry_path, skills_dir

_SAFE_NAME_RE = re.compile(r"^[a-zA-Z0-9][a-zA-Z0-9_-]*$")
_SAFE_ENV_VAR_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")

# ---------------------------------------------------------------------------
# Registry helpers
# ---------------------------------------------------------------------------


def _load_registry_for_sync(reg_path: Path | None = None) -> dict[str, Any]:
    """Load the v1 registry for sync operations."""
    path = reg_path or registry_path()
    if not path.exists():
        return {"version": "1.0.0", "mcp_definitions": {}, "skill_definitions": {}}
    with open(path) as f:
        return json.load(f)


# ---------------------------------------------------------------------------
# Launcher script generation
# ---------------------------------------------------------------------------


def _secret_lookup_command(mcp_name: str, var: str) -> str:
    """Return a platform-specific shell command to fetch a secret at runtime."""
    if not _SAFE_NAME_RE.match(mcp_name):
        msg = f"Unsafe MCP name for shell embedding: {mcp_name!r}"
        raise ValueError(msg)
    if not _SAFE_ENV_VAR_RE.match(var):
        msg = f"Unsafe env var name for shell embedding: {var!r}"
        raise ValueError(msg)

    if platform.system() == "Darwin":
        return f"$(security find-generic-password -s crux.{mcp_name} -a {var} -w 2>/dev/null)"
    return f"$(secret-tool lookup service crux.{mcp_name} username {var} 2>/dev/null)"


def generate_launcher(
    mcp_name: str,
    mcp_data: dict[str, Any],
    *,
    launcher_base: Path | None = None,
) -> Path | None:
    """Generate a platform-aware launcher script for an authed MCP."""
    env_vars = mcp_data.get("auth", {}).get("env_vars", [])
    if not env_vars:
        return None

    launcher_base = launcher_base or launchers_dir()
    launcher_base.mkdir(parents=True, exist_ok=True)

    command = mcp_data.get("command", "")
    args = list(mcp_data.get("args", []))

    if mcp_data.get("source_dir"):
        import os

        source_abs = Path(mcp_data["source_dir"])
        if not source_abs.is_absolute():
            source_abs = crux_home().parent / mcp_data["source_dir"]
        rel = os.path.relpath(source_abs, launcher_base)
        args = [f'"$SCRIPT_DIR/{rel}"' if a == "{source_dir}" else shlex.quote(a) for a in args]
    else:
        args = [shlex.quote(a) for a in args]

    export_lines = "\n".join(f"export {var}={_secret_lookup_command(mcp_name, var)}" for var in env_vars)
    exec_line = " ".join([shlex.quote(command)] + args + ['"$@"'])

    script = f"""#!/bin/bash
# Auto-generated by crux sync — do not edit. Re-run 'crux sync' to regenerate.
SCRIPT_DIR="$(cd "$(dirname "${{BASH_SOURCE[0]}}")" && pwd)"
{export_lines}
exec {exec_line}
"""
    safe_name = Path(mcp_name).name
    launcher_path = launcher_base / f"{safe_name}.sh"
    launcher_path.write_text(script)
    launcher_path.chmod(0o700)
    return launcher_path


